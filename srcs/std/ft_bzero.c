#include "libft.h"

#ifdef FT_STD
void	ft_bzero(void *s, U64 n)
{
	U64		i;

	if (n <= 0 || !s)
		return ;
	i = 0;
	while (i < n)
	{
		((string)s)[i] = '\0';
		i++;
	}
}
#endif
