#include "libft_int.h"

inline F32	ft_dot2(v2f a, v2f b)
{
	return (a[0] * b[0] + a[1] * b[1]);
}

inline F32	ft_dot3(v2f a, v2f b)
{
	return (a[0] * b[0] + a[1] * b[1] + a[2] * b[2]);
}
