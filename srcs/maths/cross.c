#include "libft_int.h"

inline v3f ft_cross3(v3f a, v3f b)
{
	return (v3f){
		a[1] * b[2] - a[2] * b[1],
		a[2] * b[0] - a[0] * b[2],
		a[0] * b[1] - a[1] * b[0]};
}
