#include "libft_int.h"

S32	ft_strcmp(const_string s1, const_string s2)
{
	while (*s1 && *s2)
	{
		if (*s1 != *s2)
			return *s1 - *s2;
		s1++;
		s2++;
	}
	return (!*s1 && *s2) ? -*s2 : (((*s1 && !*s2)) ? *s1 : 0);
}
